def make_c_smooth(x_nodes, c1=C1, c2=C2,
                  center=CENTER, width=WIDTH):
    """
    c(x) = c1 + (c2-c1) * sigmoid((x-center)/width)
    Smooth everywhere — autograd works correctly.
    Physical meaning: bonded interface with thin transition zone.
    """
    return c1 + (c2-c1) * torch.sigmoid(
        (x_nodes - center) / width)

